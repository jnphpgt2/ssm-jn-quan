package org.jnan.po;

public class City {
	private Integer cid;
	private String cname;
	private Integer shehui;
	private Integer jingji;
	private Integer huanjing;
	private Integer ziyuan;
	private Integer shenghuo;
	private Integer gong;
	private Double zong;
	
	public Integer getCid() {
		return cid;
	}
	public void setCid(Integer cid) {
		this.cid = cid;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public Integer getShehui() {
		return shehui;
	}
	public void setShehui(Integer shehui) {
		this.shehui = shehui;
	}
	public Integer getJingji() {
		return jingji;
	}
	public void setJingji(Integer jingji) {
		this.jingji = jingji;
	}
	public Integer getHuanjing() {
		return huanjing;
	}
	public void setHuanjing(Integer huanjing) {
		this.huanjing = huanjing;
	}
	public Integer getZiyuan() {
		return ziyuan;
	}
	public void setZiyuan(Integer ziyuan) {
		this.ziyuan = ziyuan;
	}
	public Integer getShenghuo() {
		return shenghuo;
	}
	public void setShenghuo(Integer shenghuo) {
		this.shenghuo = shenghuo;
	}
	public Integer getGong() {
		return gong;
	}
	public void setGong(Integer gong) {
		this.gong = gong;
	}
	public Double getZong() {
		return zong;
	}
	public void setZong(Double zong) {
		this.zong = zong;
	}
	
}
