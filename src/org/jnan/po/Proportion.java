public class Proportion {
	private Double shehui;
	private Double jingji;
	private Double huanjing;
	private Double ziyuan;
	private Double shenghuo;
	private Double gong;
	public Double getShehui() {
		return shehui;
	}
	public void setShehui(Double shehui) {
		this.shehui = shehui;
	}
	public Double getJingji() {
		return jingji;
	}
	public void setJingji(Double jingji) {
		this.jingji = jingji;
	}
	public Double getHuanjing() {
		return huanjing;
	}
	public void setHuanjing(Double huanjing) {
		this.huanjing = huanjing;
	}
	public Double getZiyuan() {
		return ziyuan;
	}
	public void setZiyuan(Double ziyuan) {
		this.ziyuan = ziyuan;
	}
	public Double getShenghuo() {
		return shenghuo;
	}
	public void setShenghuo(Double shenghuo) {
		this.shenghuo = shenghuo;
	}
	public Double getGong() {
		return gong;
	}
	public void setGong(Double gong) {
		this.gong = gong;
	}
	
}
